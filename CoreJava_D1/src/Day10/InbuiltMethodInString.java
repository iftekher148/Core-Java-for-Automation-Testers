package Day10;

public class InbuiltMethodInString {

	public static void main(String[] args) {
		
		String str1="  i Have pen  ";
	
		//trim()--It removes beginning and ending spaces of this string.
		System.out.println(str1);
		System.out.println(str1.trim());
		
		// charAt(int index)--> It returns char value for the particular index
		// It will return single Char on a specified index
		
		String str2="JavaProgramming Lanuage";
		System.out.println(str2.charAt(0));

		


	}

}
