package Day3;

public class DataTypeVar {

	public static void main(String[] args) {
	// Var data type use in Java 10 to up
		// it's  can use assign only. not to declare & assign 
		
		var b=10;
		System.out.println(b);
		var d= 9568867.6778;
		System.out.println(d);
		
		// can't declare and assing. it know only right side
		
		var n; //show error
		n=888;
		System.out.println(n);
		

	}

}
